package com.hr.bean;

public class Position {
	private Integer positionId;
	private String positionName;
	private String higherPosition;
	private String positionDuty;
	private String positionQualification;
	private String positionPower;
	private String positionContent;
	public Integer getPositionId() {
		return positionId;
	}
	public void setPositionId(Integer positionId) {
		this.positionId = positionId;
	}
	public String getPositionName() {
		return positionName;
	}
	public void setPositionName(String positionName) {
		this.positionName = positionName;
	}
	public String getHigherPosition() {
		return higherPosition;
	}
	public void setHigherPosition(String higherPosition) {
		this.higherPosition = higherPosition;
	}
	public String getPositionDuty() {
		return positionDuty;
	}
	public void setPositionDuty(String positionDuty) {
		this.positionDuty = positionDuty;
	}
	public String getPositionQualification() {
		return positionQualification;
	}
	public void setPositionQualification(String positionQualification) {
		this.positionQualification = positionQualification;
	}
	public String getPositionPower() {
		return positionPower;
	}
	public void setPositionPower(String positionPower) {
		this.positionPower = positionPower;
	}
	public String getPositionContent() {
		return positionContent;
	}
	public void setPositionContent(String positionContent) {
		this.positionContent = positionContent;
	}
	
}
